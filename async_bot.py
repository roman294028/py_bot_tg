def search_notes(query):
    """Умный поиск по всей базе конспектов"""
    query = query.lower().strip()
    results = []
    
    # Прямое совпадение
    if query in notes:
        return [(query, notes[query], 100)]
    
    # Поиск по ключевым словам
    for key, text in notes.items():
        score = 0
        # Проверяем вхождение запроса в название темы
        if query in key:
            score += 50
        # Проверяем вхождение слов запроса в текст конспекта
        words = query.split()
        for word in words:
            if len(word) > 2 and word in key:
                score += 30
            if len(word) > 2 and word in text.lower():
                score += 10
        
        if score > 0:
            results.append((key, text, score))
    
    # Сортируем по релевантности
    results.sort(key=lambda x: x[2], reverse=True)
    return results[:5]  # Возвращаем топ-5 результатов

