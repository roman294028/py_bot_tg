async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Обработчик текстовых сообщений (УМНЫЙ ПОИСК)"""
    query = update.message.text.lower().strip()
    
    # Показываем, что бот печатает
    await update.message.chat.send_action(action="typing")
    
    # Выполняем поиск
    results = search_notes(query)
    
    if results:
        # Если есть точное совпадение с высоким рейтингом
        if results[0][2] >= 80:
            await update.message.reply_text(
                results[0][1], 
                parse_mode='Markdown',
                reply_markup=get_back_keyboard()
            )
        else:
            # Показываем несколько вариантов
            response = f"🔍 **Нашёл несколько вариантов по запросу:** '{update.message.text}'\n\n"
            
            keyboard = []
            for key, text, score in results[:5]:
                # Добавляем кнопку для каждого результата
                keyboard.append([InlineKeyboardButton(f"📘 {key.capitalize()}", callback_data=key)])
            
            keyboard.append([InlineKeyboardButton("◀️ Назад", callback_data='back_to_menu')])
            
            await update.message.reply_text(
                response + "Выбери подходящую тему:",
                parse_mode='Markdown',
                reply_markup=InlineKeyboardMarkup(keyboard)
            )
    else:
        # Если ничего не нашли
        await update.message.reply_text(
            f"❌ **По запросу '{update.message.text}' ничего не найдено**\n\n"
            f"Попробуй:\n"
            f"• использовать другие слова\n"
            f"• выбрать тему из меню\n"
            f"• написать короче (например, 'ньютон' вместо 'законы ньютона')",
            reply_markup=get_main_keyboard()
        )

