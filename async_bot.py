# Создаем приложение бота
application = Application.builder().token(TOKEN).build()

# Обработчики команд
async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "👋 **Привет!** Я бот с ПОДРОБНЫМИ конспектами по физике\n\n"
        "📝 **Напиши тему:**\n"
        "• ньютон\n• оптика\n• термодинамика\n• электричество\n"
        "• механика\n• магнетизм\n• квантовая\n• атомная\n• 9 класс\n\n"
        "⚡ Я пришлю формулы + понятное объяснение!",
        parse_mode='Markdown'
    )

async def help_command(update: Update, context: ContextTypes.DEFAULT_TYPE):
    topics = ", ".join(notes.keys())
    await update.message.reply_text(
        f"📚 **Доступные темы:**\n{topics}",
        parse_mode='Markdown'
    )

async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    topic = update.message.text.lower().strip()
    
    # Ищем точное совпадение
    if topic in notes:
        await update.message.reply_text(notes[topic], parse_mode='Markdown')
        return
    
    # Ищем по ключевым словам
    for key in notes.keys():
        if key in topic:
            await update.message.reply_text(notes[key], parse_mode='Markdown')
            return
    
    # Если не нашли
    topics_list = "\n".join([f"• {t}" for t in notes.keys()])
    await update.message.reply_text(
        f"❌ **Тема '{topic}' не найдена**\n\n"
        f"📋 **Доступные темы:**\n{topics_list}\n\n"
        f"👉 Попробуй: ньютон, оптика, механика, 9 класс",
        parse_mode='Markdown'
    )

# Регистрируем обработчики
application.add_handler(CommandHandler("start", start))
application.add_handler(CommandHandler("help", help_command))
application.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))

# Запуск бота (для Bothost используем polling)
if __name__ == "__main__":
    print("🚀 Бот с ТЕКСТОВЫМИ конспектами запускается...")
    print("📚 Доступно тем:", len(notes))
    application.run_polling(allowed_updates=Update.ALL_TYPES)
